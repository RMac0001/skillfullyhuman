import { useState, ReactNode } from 'react';
import { useRouter } from 'next/router';
import { signOut, useSession } from 'next-auth/react';
import Link from 'next/link';

interface NavItem {
  name: string;
  href: string;
  icon: string;
}

interface AdminLayoutProps {
  children: ReactNode;
}

